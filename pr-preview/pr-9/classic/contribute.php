<p><li><b>Library code and patches</b>: Have you developed some classes using wxPython that might be useful to others?  Or how about changes to existing code to fix bugs or enhance features?  Send them my way and I'll include it in  wxPython if I like it.  Be sure to take a look at the <a href="codeguidelines.php">wxPython Coding Guidelines</a> first.

<p><li><b>Modules</b>: The same goes for additional extension modules.  Maybe I'm taking too long to get to adding your favorite feature of wxWidgets to wxPython.  Maybe you or somebody else has written some C++ classes using wxWidgets that you'd like to see have wxPython support...  It's now easier than ever to add new modules to wxPython.  Simply check out <a href="http://wxwidgets.org/cvs.htm">wxWidgets CVS</a>, add your code in a subdirectory of .../wxPython/contrib and you're done!  (Well, maybe you should build it and test it a couple times too.)

