<h1>wxPython Chat at irc.freenode.net</h1>

<p>
People who are familiar with wxPython and wxWidgets, and who are willing to help answer questions, can often be found in the #wxpython and #wxwidgets IRC channels at <a href="freenode.net">freenode.net</a>.  If you are not already familiar with IRC or have no clue how to get connected, what client software to use, or whatever then you'll find a simple web-based IRC client in the box below.  Simply choose a nickname, prove you are a human by solving the reCAPTCHA, and click connect.  The stuff you type in the text field at the bottom will then be relayed to all other users presently in the channel.  
</p>
<p>You can learn more about IRC at <a href="http://www.irchelp.org/">IRC Help</a>.  You may also want to read <a href="http://wiki.wxwidgets.org/IRC">this</a>.  If you expect to be using IRC more than just once or twice then registering your nickname is a good idea.  See the <a href="http://freenode.net/faq.shtml">Freenode FAQ</a> for details.
</p>
<p>
The same rules apply to IRC channels as for <a href="maillist.php">mail lists</a>, namely: be polite, be patient, be helpful, be kind, and avoid the 3 W's: no whining, no whinging and don't be a weenie.  In other words, do not type anything that your granny might smack you for if she saw you do it.
</p>

